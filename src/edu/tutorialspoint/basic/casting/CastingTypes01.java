package edu.tutorialspoint.basic.casting;

public class CastingTypes01 {
    public static void main(String[] args) {
        int num01 = 54;
        double num02 = 2.5;
        double sum = num01 + num02;

        System.out.println("A soma de " + num01 + " e " + num02 + " é: " + sum); // A soma de 54 e 2.5 é: 56.5
    }
}
